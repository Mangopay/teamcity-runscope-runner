package com.mangopay.teamcity.runscope.model;

public class Step {
    private String id;
    private String stepType;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStepType() {
        return stepType;
    }

    public void setStepType(String stepType) {
        this.stepType = stepType;
    }
}
